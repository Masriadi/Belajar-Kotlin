fun tambah(a:Int, b:Int):Int {
    return a + b
}

fun kali(a:Int, b:Int):Int {
    return a * b
}

fun bagi(a:Int, b:Int):Int {
    return a / b
}

fun kurang(a:Int, b:Int):Int {
    return a - b
}

fun main() {
    println(tambah(3, 5))
    println(kali(3, 5))
    println(bagi(10, 5))
    println(kurang(10, 5))
}